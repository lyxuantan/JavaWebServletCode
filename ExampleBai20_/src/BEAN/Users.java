package BEAN;

public class Users 
{
		private int stt;
		private String name;
		private int pass;
		public int getStt() {
			return stt;
		}
		public void setStt(int stt) {
			this.stt = stt;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getPass() {
			return pass;
		}
		public void setPass(int pass) {
			this.pass = pass;
		}
		
}
